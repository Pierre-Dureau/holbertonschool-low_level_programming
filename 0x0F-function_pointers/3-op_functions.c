#include "3-calc.h"

/**
 * op_add - Addition of two number
 *
 * @a: First number
 * @b: Second number
 * Return: Result of the calcul
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substraction of two number
 *
 * @a: First number
 * @b: Second number
 * Return: Result of the calcul
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication of two number
 *
 * @a: First number
 * @b: Second number
 * Return: Result of the calcul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of two number
 *
 * @a: First number
 * @b: Second number
 * Return: Result of the calcul
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Remainder of the divison of two number
 *
 * @a: First number
 * @b: Second number
 * Return: Result of the calcul
 */

int op_mod(int a, int b)
{
	return (a % b);
}
